enum {
	SPD_INFO_DDR4,
	SPD_INFO_DEFAULT,
};

static const struct spd_info {
	size_t spd_len;
	uint32_t spd_part_off;
	size_t spd_part_len;
} spd_mem_info[] = {
	[SPD_INFO_DDR4] = {
		.spd_len = SPD_DDR4_LENGTH,
		.spd_part_off = SPD_DDR4_PART_OFF,
		.spd_part_len = SPD_DDR4_PART_LEN,
	},
	[SPD_INFO_DEFAULT] = {
		.spd_len = SPD_DEFAULT_LENGTH,
		.spd_part_off = SPD_DEFAULT_PART_OFF,
		.spd_part_len = SPD_DEFAULT_PART_LEN,
	},
};

static int kahlee_spd_read(struct platform_intf *intf, int dimm, int reg,
			   int spd_len, uint8_t *spd_buf)
{
	static uint8_t *fw_buf;
	static int fw_size = 0;
	const char *smbios_part_num;
	size_t smbios_part_num_len, spd_part_num_len;
	ssize_t offset;
	uint8_t i, num_spd;
	const uint8_t *ptr, *spd_part_num;
	struct smbios_table *table;
	const struct spd_info *info = &spd_mem_info[SPD_INFO_DEFAULT];
	int retval;

	/* dimm cnt is 0 based */
	if (dimm >= intf->cb->memory->dimm_count(intf)) {
		lprintf(LOG_DEBUG, "%s: Invalid DIMM specified\n", __func__);
		return -1;
	}

	if (fw_size < 0)
		return -1; /* previous attempt failed */

	if (!fw_size) {
		fw_size = flashrom_read_host_firmware_region(intf, &fw_buf);
		if (fw_size < 0)
			return -1;
		add_destroy_callback(free, fw_buf);
	}

	retval = spd_read_from_cbfs(intf, dimm, reg, spd_len, spd_buf,
			fw_size, fw_buf);
	if (retval > 0)
		return retval;

	table = mosys_malloc(sizeof(*table));

	// if not found in CBFS, use the above table to find SPD information
	lprintf(LOG_DEBUG, "Use SMBIOS type 17 to get memory information\n");
	if (smbios_find_table(intf, SMBIOS_TYPE_MEMORY, dimm, table,
			SMBIOS_LEGACY_ENTRY_BASE,
			SMBIOS_LEGACY_ENTRY_LEN) < 0) {
		lprintf(LOG_DEBUG, "Can't find smbios type17\n");
		free(table);
		return -1;
	}

	if (table->data.mem_device.type == SMBIOS_MEMORY_TYPE_DDR4)
		info = &spd_mem_info[SPD_INFO_DDR4];

	smbios_part_num = table->string[table->data.mem_device.part_number];
	smbios_part_num_len = strnlen(smbios_part_num, info->spd_part_len);

	if (smbios_part_num_len == 0) {
		free(table);
		return -1;
	}

	// Legacy firmware doesn't remove trailing whitespace from SPD part
	// number so needs to check again; otherwise the part number might never
	// matches to spd_part_num[] below which did remove trailing whitespace.
	while (smbios_part_num_len > 0
			&& smbios_part_num[smbios_part_num_len - 1] == ' ')
		smbios_part_num_len--;

	num_spd = sizeof(GRUNT_SPDS) / info->spd_len;

	for (i = 0; i < num_spd; i++) {
		offset = i * info->spd_len;
		ptr = &GRUNT_SPDS[offset];
		spd_part_num = ptr + info->spd_part_off;
		spd_part_num_len = info->spd_part_len;

		// Strip off trailing whitespace from SPD part number.
		while (spd_part_num_len > 0
				&& spd_part_num[spd_part_num_len - 1] == ' ')
			spd_part_num_len--;

		if (spd_part_num_len != smbios_part_num_len)
			continue;
		if (!memcmp(smbios_part_num, spd_part_num, smbios_part_num_len))
			break;
	}

	free(table);

	if (i == num_spd)
		return -1;

	memcpy(spd_buf, &GRUNT_SPDS[offset], spd_len);
	return spd_len;
}

static struct memory_spd_cb kahlee_spd_cb = {
	.read   = kahlee_spd_read,
};

struct memory_cb kahlee_memory_cb = {
	.dimm_count		= smbios_dimm_count,
	.dimm_speed		= smbios_dimm_speed,
	.spd			= &kahlee_spd_cb,
};
